#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	s2 = ft_strnew(i);
	while (j <= i)
	{
		s2[j] = s1[j];
		j++;
	}
	return (s2);
}
